package com.xworkz.inheritanceTypes.heirarchical;

public class Fish {
    private String species;
    private int age;

    public Fish(String species, int age) {
        this.species = species;
        this.age = age;
    }

    public String getSpecies() {
        return species;
    }

    public int getAge() {
        return age;
    }
}
